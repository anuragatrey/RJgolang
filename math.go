package main

import "fmt"

func main() {

	FirstNumber := 21
	SecondNumber := 10

	fmt.Println("Sum of Two number:", FirstNumber+SecondNumber)
	fmt.Println("Sub of Two number:", FirstNumber-SecondNumber)
	fmt.Println("Mul of Two number:", FirstNumber*SecondNumber)
	fmt.Println("Div of Two number:", FirstNumber/SecondNumber)
	fmt.Println("Modulus(Remainder):", FirstNumber%SecondNumber)

}
